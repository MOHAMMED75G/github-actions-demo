/**
 * A simple math module with basic arithmetic functions
 */

/**
 * Adds two numbers and returns the result
 * @param {number} a - The first number
 * @param {number} b - The second number
 * @returns {number} The sum of a and b
 */
function add(a, b) {
  return a + b;
}

module.exports = {
  add
};

